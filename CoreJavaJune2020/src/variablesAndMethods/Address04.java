package variablesAndMethods;

public class Address04 { //Opening of Class body

	// Class body
	// CLASS BODY CONTAIN -- 
	
	// Variables - declared / Initialize
	// Constructor -- declared / Initialize
	// method -- Implemented/ Initialize  -- 
	// in Class method is not declared like variables and constructor
	
	static String Name = "Tofael";  //String inside double quotation, 
	static int age = 44;  // int without any quotation
	static char sex = 'M';  //char with single quotation
	static double grade = 3.876;  //double no quotation
	//Variable declared before main method and they are static variable
	public static void main(String[] args) {
		System.out.println(Name+age+sex+grade);  
		System.out.println("My Name: " +Name + ", My age: " +age+ ", My sex: " +sex+ ", My grade: " +grade); // bring all the variable in one sysout
		
	}
	

} //closing of class body